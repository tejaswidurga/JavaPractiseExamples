package pk3Operators;

public class Ex2AssignmentExample {
	public static void main(String [] args) {
		int num1=12;
		int num2=num1;
		System.out.println(num2);
		num2 += num1;
		System.out.println(num2);
		num2 -=num1;
		System.out.println(num2);
		num2 *=num1;
		System.out.println(num2);
		num2 /=num1;
		System.out.println(num2);
		num2 %=num1;
		System.out.println(num2);
	}

}
